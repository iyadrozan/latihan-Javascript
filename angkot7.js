var Angkot = parseInt(prompt("Masukan Nomor angkot yang ingin kamu pesan: "));

if (Angkot === 1) {
  alert("Anda memesan angkot nomor 1");
} else if (Angkot === 2) {
  alert("Anda memasukan angkot nomor 2");
} else if (Angkot === 3) {
  alert("Anda memasukan angkot nomor 3");
} else {
  alert("Nomor angkot yang anda masukan tidak terdaftar");
}
